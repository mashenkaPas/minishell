#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*curr;
	t_list	*tail;
	t_list	*head;

	head = NULL;
	if (!lst || !f)
		return (NULL);
	while (lst)
	{
		if (!(tail = malloc(sizeof(size_t))))
			return (NULL);
		tail = f(lst);
		if (!head)
		{
			head = tail;
			curr = tail;
		}
		else
		{
			curr->next = tail;
			curr = curr->next;
		}
		lst = lst->next;
	}
	return (head);
}
