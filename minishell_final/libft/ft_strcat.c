#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	j;
	int	i;

	i = 0;
	if (s2[i] == '\0')
		return (s1);
	j = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[j + i] = s2[i];
		i++;
	}
	s1[j + i] = '\0';
	return (s1);
}
